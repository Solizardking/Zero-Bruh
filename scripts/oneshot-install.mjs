/**
 * Copy skill pack into installDir/skills and symlink each skill into agents dirs.
 */
function installSkillPack({ installDir, force }) {
  const destSkills = join(installDir, "skills");
  ensureDir(installDir);

  if (existsSync(destSkills) && force) {
    rmSync(destSkills, { recursive: true, force: true });
  }

  if (!existsSync(SKILLS_DIR)) {
    die(`Bundled skills missing at ${SKILLS_DIR}`);
  }

  // Prefer full tree copy so pack-index + catalog ship together.
  cpSync(SKILLS_DIR, destSkills, {
    recursive: true,
    force: true,
    filter: (src) => !src.includes("node_modules"),
  });

  const pack = loadPackIndex();
  const ids = listSkillIds();
  const agentRoots = [
    join(homedir(), ".agents", "skills"),
    join(homedir(), ".claude", "skills"),
    join(homedir(), ".codex", "skills"),
  ];

  let linked = 0;
  for (const root of agentRoots) {
    try {
      ensureDir(root);
    } catch {
      continue;
    }
    for (const id of ids) {
      const src = join(destSkills, id);
      const dst = join(root, id);
      try {
        if (!existsSync(src) || !statSync(src).isDirectory()) continue;
      } catch {
        continue;
      }
      // Safe link: prefer symlink; never crash the oneshot on FS quirks (macOS
      // "equivalent: Operation not supported" when comparing cross-volume paths).
      try {
        // lstat via existsSync can throw on dangling multi-volume links ‚ÄĒ probe carefully
        let destExists = false;
        try {
          destExists = existsSync(dst);
        } catch {
          destExists = false;
        }
        if (destExists) {
          if (!force) continue;
          try {
            rmSync(dst, { recursive: true, force: true });
          } catch {
            continue;
          }
        }
        try {
          symlinkSync(src, dst, "dir");
          linked += 1;
          continue;
        } catch {
          /* fall through to copy */
        }
        try {
          cpSync(src, dst, { recursive: true, force: true, errorOnExist: false });
          linked += 1;
        } catch {
          /* skip single skill */
        }
      } catch {
        /* never fail oneshot on agent skill linking */
      }
    }
  }

  return {
    skillsDir: destSkills,
    skillCount: ids.length,
    packId: pack.id,
    linked,
    agentRoots,
  };
}

function installAutomaton({ packageRoot, skip }) {
  if (skip) return { skipped: true };
  const autoDir = join(packageRoot, "automaton-main");
  if (!existsSync(join(autoDir, "package.json"))) {
    return { skipped: true, reason: "automaton-main not in package" };
  }
  if (!commandExists("npm")) {
    return { skipped: true, reason: "npm not found" };
  }
  info("Installing automaton-main dependencies...");
  const r = spawnSync("npm", ["install", "--legacy-peer-deps", "--ignore-scripts"], {
    cwd: autoDir,
    stdio: "inherit",
    env: process.env,
  });
  return { ok: r.status === 0, status: r.status, dir: autoDir };
}

function tryBuildGoBinary({ packageRoot, installDir, skip }) {
  if (skip) return { skipped: true };
  if (!commandExists("go")) {
    warn("Go not found ‚ÄĒ skip clawdbot binary (install Go then: go build -o ~/.clawdbot/bin/clawdbot ./cmd/clawdbot)");
    return { skipped: true, reason: "go not found" };
  }
  const cmdDir = join(packageRoot, "cmd", "clawdbot");
  if (!existsSync(cmdDir)) {
    warn("cmd/clawdbot not in this package ‚ÄĒ use full git install for binary:");
    warn("  curl -fsSL https://raw.githubusercontent.com/Solizardking/clawdbot-go/main/install.sh | bash");
    return { skipped: true, reason: "go sources not shipped in npm pack" };
  }
  const binDir = join(installDir, "bin");
  ensureDir(binDir);
  const out = join(binDir, "clawdbot");
  info("Building clawdbot Go binary...");
  const r = spawnSync(
    "go",
    ["build", "-buildvcs=false", "-trimpath", "-ldflags=-s -w", "-o", out, "./cmd/clawdbot/"],
    { cwd: packageRoot, stdio: "inherit", env: process.env },
  );
  if (r.status !== 0) {
    warn("Go build failed");
    return { ok: false, status: r.status };
  }
  const localBin = join(homedir(), ".local", "bin");
  ensureDir(localBin);
  try {
    copyFileSync(out, join(localBin, "clawdbot"));
  } catch {
    /* ignore */
  }
  ok(`Binary: ${out}`);
  return { ok: true, path: out };
}

function seedBirthSkills({ skip }) {
  if (skip) return { skipped: true };
  if (!commandExists("npx")) {
    return { skipped: true, reason: "npx not found" };
  }
  info("Seeding birth skills (Solizardking/skills + Go skills)...");
  const sources = [
    ["skills", "add", "https://github.com/Solizardking/skills", "--all"],
    ["skills", "add", "https://github.com/samber/cc-skills-golang", "--all"],
  ];
  const results = [];
  for (const args of sources) {
    const r = spawnSync("npx", ["--yes", ...args], {
      stdio: "inherit",
      env: process.env,
      timeout: 120_000,
    });
    results.push({ args, status: r.status });
    if (r.status !== 0) warn(`Birth seed failed: npx ${args.join(" ")}`);
  }
  return { ok: results.every((x) => x.status === 0), results };
}

