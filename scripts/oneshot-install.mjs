export function runOneshot(options = {}) {
  const flags = {
    skipGo: Boolean(options.skipGo),
    skipBirth: Boolean(options.skipBirth),
    skipAutomaton: Boolean(options.skipAutomaton),
    force: Boolean(options.force),
    dir: options.dir || process.env.CLAWDBOT_INSTALL_DIR || join(homedir(), ".clawdbot"),
  };

  printBanner();

  const inspect = inspectPack();
  if (!inspect.ok) {
    warn(`Pack inspection issues: ${inspect.missing.join(", ")}`);
  } else {
    ok(`Skill pack OK — ${inspect.skillCount} skills (${inspect.packId})`);
  }

  const installDir = resolve(flags.dir);
  ensureDir(installDir);
  info(`Install home: ${installDir}`);

  const skillResult = installSkillPack({ installDir, force: flags.force });
  ok(`Skills installed → ${skillResult.skillsDir} (${skillResult.skillCount})`);
  ok(`Linked into agent skill dirs (${skillResult.linked} links)`);

  const envPath = join(installDir, ".env");
  const envResult = writeEnvTemplate(envPath, {
    force: flags.force,
    installDir,
    skillsDir: skillResult.skillsDir,
    entries: {
      CLAWDBOT_HOME: installDir,
      CLAWDBOT_SKILLS_DIR: skillResult.skillsDir,
      CLAWDBOT_MERGE_BUNDLED_SKILLS: "1",
    },
  });
  if (envResult.created) ok(`.env written → ${envPath}`);
  else ok(`.env updated (${envResult.added} keys) → ${envPath}`);

  // Symlink ~/.clawdbot if installDir is custom
  const defaultHome = join(homedir(), ".clawdbot");
  if (installDir !== defaultHome) {
    try {
      if (!existsSync(defaultHome)) {
        symlinkSync(installDir, defaultHome, "dir");
        ok(`Linked ${defaultHome} → ${installDir}`);
      }
    } catch {
      /* ignore */
    }
  }

  const auto = installAutomaton({ packageRoot: PACKAGE_ROOT, skip: flags.skipAutomaton });
  if (auto.ok) ok(`automaton-main deps at ${auto.dir}`);
  else if (auto.skipped) info(`automaton: skipped (${auto.reason || "flag"})`);

  const go = tryBuildGoBinary({
    packageRoot: PACKAGE_ROOT,
    installDir,
    skip: flags.skipGo,
  });

  const birth = seedBirthSkills({ skip: flags.skipBirth });
  if (birth.ok) ok("Birth skill seeds complete");
  else if (birth.skipped) info(`Birth seeds skipped (${birth.reason || "flag"})`);

  // Write install receipt
  const receipt = {
    installedAt: new Date().toISOString(),
    packageRoot: PACKAGE_ROOT,
    installDir,
    skillsDir: skillResult.skillsDir,
    skillCount: skillResult.skillCount,
    packId: skillResult.packId,
    go: go.ok ? go.path : null,
    productHost: "https://funpump.ai",
    terminalHost: "https://cheshireterminal.ai",
    agentHub: "https://cheshireterminal.ai/agents",
    forgeHost: "https://cheshireterminal.ai/agents/forge",
    zeroClawdHost: "https://cheshireterminal.ai/zeroclawd",
    agentsNpm: "https://www.npmjs.com/package/cheshire-terminal-agents",
    agentsRepo: "https://github.com/Solizardking/Cheshire-Terminal-Agents",
    skillHubRepo: "https://github.com/Solizardking/skillhub-main",
    oneShot: "curl -fsSL https://raw.githubusercontent.com/Solizardking/Zero-Bruh/main/install-npm.sh | bash",
  };
  writeFileSync(join(installDir, "oneshot-receipt.json"), JSON.stringify(receipt, null, 2));

  console.log();
  console.log(`${GREEN}${BOLD}  ══════════════════════════════════════════${RESET}`);
  console.log(`${GREEN}${BOLD}  🦞 Zero Clawd stack installed (npm oneshot)${RESET}`);
  console.log(`${GREEN}${BOLD}  ══════════════════════════════════════════${RESET}`);
  console.log();
  console.log(`  ${BOLD}Get started:${RESET}`);
  console.log(`  ${CYAN}export CLAWDBOT_SKILLS_DIR="${skillResult.skillsDir}"${RESET}`);
  console.log(`  ${CYAN}source ${envPath}${RESET}`);
  console.log(`  ${CYAN}npx clawdbot-go skills${RESET}              # list RH pack`);
  console.log(`  ${CYAN}clawdbot catalog skills${RESET}             # if Go binary present`);
  console.log(`  ${CYAN}# Set BLOCKSCOUT_API_KEY + RH_RPC_URL for RH 4663${RESET}`);
  console.log();
  console.log(`  ${BOLD}Surfaces:${RESET}`);
  console.log(`    Zero Clawd   https://cheshireterminal.ai/zeroclawd`);
  console.log(`    Agent hub    https://cheshireterminal.ai/agents`);
  console.log(`    Agent forge  https://cheshireterminal.ai/agents/forge`);
  console.log(`    FunPump      https://funpump.ai`);
  console.log(`  ${BOLD}Open agents:${RESET}`);
  console.log(`    npm          https://www.npmjs.com/package/cheshire-terminal-agents`);
  console.log(`    GitHub       https://github.com/Solizardking/Cheshire-Terminal-Agents`);
  console.log(`    SkillHub     https://github.com/Solizardking/skillhub-main`);
  console.log(`  ${BOLD}Skills:${RESET}   ${skillResult.skillCount} in ${skillResult.skillsDir}`);
  console.log(`  ${BOLD}Full Go:${RESET}  curl -fsSL https://raw.githubusercontent.com/Solizardking/Zero-Bruh/main/install.sh | bash`);
  console.log(`  ${BOLD}npm package:${RESET} npm i -g clawdbot-go && clawdbot-go install`);
  console.log(`  ${BOLD}Agents SDK:${RESET}  npm i cheshire-terminal-agents`);
  console.log(`  ${BOLD}SkillHub:${RESET}     npx github:Solizardking/skills install cheshire-terminal-agents --force`);
  console.log();
  console.log(`  🦞 $CLAWD :: Droids Lead The Way`);
  console.log();

  return receipt;
}

