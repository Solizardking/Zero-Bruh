export function main(argv = process.argv.slice(2)) {
  const cmd = argv[0];
  if (!cmd || cmd === "help" || cmd === "--help" || cmd === "-h") {
    if (cmd === "help" || cmd === "--help" || cmd === "-h") {
      printHelp();
      return;
    }
  }

  if (cmd === "skills" || cmd === "list") {
    console.log(listSkillIds().join("\n"));
    return;
  }
  if (cmd === "inspect") {
    console.log(JSON.stringify(inspectPack(), null, 2));
    return;
  }
  if (cmd === "skills-dir") {
    console.log(SKILLS_DIR);
    return;
  }
  if (cmd === "skills-install" || cmd === "prepackage-skills") {
    const flags = parseArgs(argv.slice(1));
    const installDir =
      flags.dir || process.env.CLAWDBOT_INSTALL_DIR || join(homedir(), ".clawdbot");
    const result = installSkillPackOnly({ installDir, force: flags.force });
    console.log(
      JSON.stringify(
        {
          ok: true,
          skillsDir: result.skillsDir,
          skillCount: result.skillCount,
          linked: result.linked,
          packId: result.packId,
        },
        null,
        2,
      ),
    );
    return;
  }

  const flags = parseArgs(argv);
  if (flags.help) {
    printHelp();
    return;
  }
  runOneshot(flags);
}

const isMain =
  process.argv[1] &&
  resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isMain) {
  main();
}
