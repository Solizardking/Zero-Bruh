const __dirname = dirname(fileURLToPath(import.meta.url));

const CYAN = "\x1b[36m";
const GREEN = "\x1b[32m";
const YELLOW = "\x1b[33m";
const RED = "\x1b[31m";
const BOLD = "\x1b[1m";
const RESET = "\x1b[0m";

function info(msg) {
  console.log(`${CYAN}  ▶${RESET} ${msg}`);
}
function ok(msg) {
  console.log(`${GREEN}  ✓${RESET} ${msg}`);
}
function warn(msg) {
  console.log(`${YELLOW}  ⚠${RESET} ${msg}`);
}
function die(msg) {
  console.error(`${RED}  ✗ ERROR:${RESET} ${msg}`);
  process.exit(1);
}

function parseArgs(argv) {
  const flags = {
    skipGo: false,
    skipBirth: false,
    skipAutomaton: false,
    force: false,
    dir: "",
    help: false,
  };
  for (let i = 0; i < argv.length; i += 1) {
    const a = argv[i];
    if (a === "--help" || a === "-h") flags.help = true;
    else if (a === "--skip-go") flags.skipGo = true;
    else if (a === "--skip-birth") flags.skipBirth = true;
    else if (a === "--skip-automaton") flags.skipAutomaton = true;
    else if (a === "--force") flags.force = true;
    else if (a === "--dir" || a === "--install-dir") {
      flags.dir = argv[i + 1] || "";
      i += 1;
    } else if (a === "install" || a === "oneshot") {
      /* subcommand alias */
    } else if (a.startsWith("-")) {
      die(`Unknown flag: ${a}`);
    }
  }
  return flags;
}

function hasCmd(name) {
  const r = spawnSync(name, ["--version"], { stdio: "ignore", shell: false });
  return r.status === 0 || r.error === undefined && (r.status === 0 || r.status === null);
}

function which(name) {
  const r = spawnSync(process.platform === "win32" ? "where" : "command", 
    process.platform === "win32" ? [name] : ["-v", name],
    { encoding: "utf8", shell: false },
  );
  if (r.status !== 0) return "";
  return String(r.stdout || "").trim().split("\n")[0] || "";
}

function commandExists(cmd) {
  try {
    const r = spawnSync(
      process.platform === "win32" ? "where" : "sh",
      process.platform === "win32" ? [cmd] : ["-c", `command -v ${cmd}`],
      { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] },
    );
    return r.status === 0 && String(r.stdout || "").trim().length > 0;
  } catch {
    return false;
  }
}

function ensureDir(path) {
  mkdirSync(path, { recursive: true });
}

function appendEnvIfMissing(envPath, key, value) {
  if (!value) return false;
  let body = existsSync(envPath) ? readFileSync(envPath, "utf8") : "";
  if (new RegExp(`^${key}=`, "m").test(body)) return false;
  if (body && !body.endsWith("\n")) body += "\n";
  body += `${key}=${value}\n`;
  writeFileSync(envPath, body, { mode: 0o600 });
  return true;
}

function writeEnvTemplate(envPath, values) {
  if (existsSync(envPath) && !values.force) {
    let added = 0;
    for (const [k, v] of Object.entries(values.entries)) {
      if (appendEnvIfMissing(envPath, k, v)) added += 1;
    }
    return { created: false, added };
  }
  const lines = [
    "# ════════════════════════════════════════════════════════════════════",
    "# Zero Clawd / clawdbot-go — generated by oneshot npm installer",
    "# ════════════════════════════════════════════════════════════════════",
    "",
    `# Install home: ${values.installDir}`,
    `CLAWDBOT_HOME=${values.installDir}`,
    `CLAWDBOT_SKILLS_DIR=${values.skillsDir}`,
    `CLAWDBOT_MERGE_BUNDLED_SKILLS=1`,
    "",
    "# Robinhood Chain (4663) — set for launch/deploy/trade",
    "# BLOCKSCOUT_API_KEY=proapi_…",
    "# RH_RPC_URL=https://your-private-rpc",
    "",
    "# Free AI via zkrouter (optional key from install API)",
    "ZKROUTER_BASE_URL=https://clawdrouter-zk.fly.dev/v1",
    "",
    "# Product surfaces",
    "CLAWD_TERMINAL_URL=https://cheshireterminal.ai",
    "FUNPUMP_URL=https://funpump.ai",
    "",
  ];
  for (const [k, v] of Object.entries(values.entries)) {
    if (v && !lines.some((l) => l.startsWith(`${k}=`))) {
      lines.push(`${k}=${v}`);
    }
  }
  lines.push("");
  writeFileSync(envPath, lines.join("\n"), { mode: 0o600 });
  return { created: true, added: Object.keys(values.entries).length };
}

/**
 * Skills-only prepackage (used by npm postinstall + `npx clawdbot-go skills-install`).
 * Copies the full bundled skills tree and links every SKILL.md into agent roots.
 */
export function installSkillPackOnly({ installDir, force = false } = {}) {
  return installSkillPack({
    installDir: installDir || join(homedir(), ".clawdbot"),
    force,
  });
}

/**
 * Copy skill pack into installDir/skills and symlink each skill into agents dirs.
 * Installs pack-index skills plus any extra on-disk SKILL.md directories.
 */
function installSkillPack({ installDir, force }) {
  const destSkills = join(installDir, "skills");
  ensureDir(installDir);

  if (existsSync(destSkills) && force) {
    rmSync(destSkills, { recursive: true, force: true });
  }

  if (!existsSync(SKILLS_DIR)) {
    die(`Bundled skills missing at ${SKILLS_DIR}`);
  }

  // Prefer full tree copy so pack-index + catalog ship together.
  // Do NOT filter on "node_modules" in the absolute path — when installed from
  // npm the package lives at …/node_modules/clawdbot-go/skills and a naive
  // includes("node_modules") filter would drop every file.
  ensureDir(destSkills);
  cpSync(SKILLS_DIR, destSkills, {
    recursive: true,
    force: true,
  });

  const pack = loadPackIndex();
  const fromIndex = listSkillIds();
  // Union pack-index + every directory that actually has SKILL.md
  const onDisk = readdirSync(destSkills)
    .filter((name) => {
      const dir = join(destSkills, name);
      try {
        return statSync(dir).isDirectory() && existsSync(join(dir, "SKILL.md"));
      } catch {
        return false;
      }
    })
    .sort();
  const ids = [...new Set([...fromIndex, ...onDisk])];

  const agentRoots = [
    join(homedir(), ".agents", "skills"),
    join(homedir(), ".claude", "skills"),
    join(homedir(), ".codex", "skills"),
  ];

  // Link into agent hosts in one shell batch — Node fs.equivalent can abort on
  // macOS multi-volume paths; per-skill spawnSync is also slow.
  let linked = 0;
  if (process.env.CLAWDBOT_SKIP_AGENT_LINKS !== "1") {
    const script = [
      "set +e",
      ...agentRoots.map((root) => `mkdir -p ${JSON.stringify(root)}`),
      ...agentRoots.flatMap((root) =>
        ids.map((id) => {
          const src = join(destSkills, id);
          const dst = join(root, id);
          return `if [ -d ${JSON.stringify(src)} ]; then ln -sfn ${JSON.stringify(src)} ${JSON.stringify(dst)} && echo linked; fi`;
        }),
      ),
    ].join("\n");
    const batch = spawnSync("bash", ["-c", script], {
      encoding: "utf8",
      timeout: 30_000,
    });
    if (batch.status === 0 || batch.stdout) {
      linked = String(batch.stdout || "")
        .split("\n")
        .filter((l) => l.trim() === "linked").length;
    }
  }

  try {
    writeFileSync(
      join(destSkills, ".clawdbot-prepackaged.json"),
      JSON.stringify(
        {
          prepackagedAt: new Date().toISOString(),
          packId: pack.id,
          skillCount: ids.length,
          packIndexCount: fromIndex.length,
          skills: ids,
          productHost: pack.productHost || "https://funpump.ai",
          terminalHost: pack.terminalHost || "https://cheshireterminal.ai",
          agentHub: pack.agentHub || "https://cheshireterminal.ai/agents",
          forgeHost: pack.forgeHost || "https://cheshireterminal.ai/agents/forge",
          zeroClawdHost: pack.zeroClawdHost || "https://cheshireterminal.ai/zeroclawd",
          agentsNpm: pack.agentsNpm || "https://www.npmjs.com/package/cheshire-terminal-agents",
          agentsRepo: pack.agentsRepo || "https://github.com/Solizardking/Cheshire-Terminal-Agents",
          skillHubRepo: pack.skillHubRepo || "https://github.com/Solizardking/skillhub-main",
        },
        null,
        2,
      ),
    );
  } catch {
    /* non-fatal */
  }

  return {
    skillsDir: destSkills,
    skillCount: ids.length,
    packId: pack.id,
    linked,
    agentRoots,
  };
}

function installAutomaton({ packageRoot, skip }) {
  if (skip) return { skipped: true };
  const autoDir = join(packageRoot, "automaton-main");
  if (!existsSync(join(autoDir, "package.json"))) {
    return { skipped: true, reason: "automaton-main not in package" };
  }
  if (!commandExists("npm")) {
    return { skipped: true, reason: "npm not found" };
  }
  info("Installing automaton-main dependencies...");
  const r = spawnSync("npm", ["install", "--legacy-peer-deps", "--ignore-scripts"], {
    cwd: autoDir,
    stdio: "inherit",
    env: process.env,
  });
  return { ok: r.status === 0, status: r.status, dir: autoDir };
}

function tryBuildGoBinary({ packageRoot, installDir, skip }) {
  if (skip) return { skipped: true };
  if (!commandExists("go")) {
    warn("Go not found — skip clawdbot binary (install Go then: go build -o ~/.clawdbot/bin/clawdbot ./cmd/clawdbot)");
    return { skipped: true, reason: "go not found" };
  }
  const cmdDir = join(packageRoot, "cmd", "clawdbot");
  if (!existsSync(cmdDir)) {
    warn("cmd/clawdbot not in this package — use full git install for binary:");
    warn("  curl -fsSL https://raw.githubusercontent.com/Solizardking/Zero-Bruh/main/install.sh | bash");
    return { skipped: true, reason: "go sources not shipped in npm pack" };
  }
  const binDir = join(installDir, "bin");
  ensureDir(binDir);
  const out = join(binDir, "clawdbot");
  info("Building clawdbot Go binary...");
  const r = spawnSync(
    "go",
    ["build", "-buildvcs=false", "-trimpath", "-ldflags=-s -w", "-o", out, "./cmd/clawdbot/"],
    { cwd: packageRoot, stdio: "inherit", env: process.env },
  );
  if (r.status !== 0) {
    warn("Go build failed");
    return { ok: false, status: r.status };
  }
  const localBin = join(homedir(), ".local", "bin");
  ensureDir(localBin);
  try {
    copyFileSync(out, join(localBin, "clawdbot"));
  } catch {
    /* ignore */
  }
  ok(`Binary: ${out}`);
  return { ok: true, path: out };
}

function seedBirthSkills({ skip }) {
  if (skip) return { skipped: true };
  if (!commandExists("npx")) {
    return { skipped: true, reason: "npx not found" };
  }
  info("Seeding birth skills (Solizardking/skills + Go skills)...");
  const sources = [
    ["skills", "add", "https://github.com/Solizardking/skills", "--all"],
    ["skills", "add", "https://github.com/samber/cc-skills-golang", "--all"],
  ];
  const results = [];
  for (const args of sources) {
    const r = spawnSync("npx", ["--yes", ...args], {
      stdio: "inherit",
      env: process.env,
      timeout: 120_000,
    });
    results.push({ args, status: r.status });
    if (r.status !== 0) warn(`Birth seed failed: npx ${args.join(" ")}`);
  }
  return { ok: results.every((x) => x.status === 0), results };
}

