#!/usr/bin/env node
/**
 * Zero Clawd — one-shot stack installer (Grok Build style).
 *
 * Installs:
 *   • RH / Cheshire skill pack (23 skills) → ~/.clawdbot/skills + ~/.agents/skills
 *   • Env template with CLAWDBOT_SKILLS_DIR, RH hooks
 *   • Optional: Go clawdbot binary, automaton-main deps, birth skill seeds
 *
 * Usage:
 *   npx clawdbot-go install
 *   node scripts/oneshot-install.mjs [--skip-go] [--skip-birth] [--dir PATH]
 *   curl -fsSL https://raw.githubusercontent.com/Solizardking/Zero-Bruh/main/install-npm.sh | bash
 */
